package com.growee.objects.notification;

public class DeviceNotification {
	boolean setNotification;
	String email;
	String userId;
	String devcieId;
	
	
	
	public boolean isSetNotification() {
		return setNotification;
	}
	public void setSetNotification(boolean setNotification) {
		this.setNotification = setNotification;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getDevcieId() {
		return devcieId;
	}
	public void setDevcieId(String devcieId) {
		this.devcieId = devcieId;
	}
	
	
	
}
